m = 15
n = 7
add = m + n
print("Addition of m and n is:", add)
sub = m - n
print("Subtraction of m and n is:", sub)
mul = m * n
print("Multiplication of m and n is:", mul)
div = m / n
print("Division of m and n is:", div)
exp = m ** n
print("Exponential of m and n is:", exp)
modu = m % n
print("Modulus of m and n is:", modu)
floordiv = m // n
print("Floor Division of m and n is:", floordiv)